// 裁切 wardrobe layer 素材的透明留白，並記錄每張圖裁切前的「原始內容框」（canvas 512x768 座標）。
//
// 用途（issue #176）：
//   現有素材多為 512x768、衣物本體置於人物對應位置、四周大量透明留白；留白會讓 contain
//   縮放對位不可預測。本腳本把每張圖裁成「衣物本體的緊貼框」，並輸出其原始內容框，供引擎以
//   per-item targetBox 對位——targetBox = 原始內容框時即與裁切前畫面完全一致（identity 遷移）。
//
// 量測：以 alpha 通道門檻取非透明範圍（忽略次可見雜訊）。需系統有 ImageMagick `magick`。
//
//   node tool/trim-wardrobe-assets.mjs            # dry-run：只量測與回報，不改檔
//   node tool/trim-wardrobe-assets.mjs --apply    # 實際裁切覆寫素材，並產生 content-box 對照模組

import { execFileSync } from "node:child_process";
import { readdirSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = process.cwd();
const WARDROBE_DIR = join(ROOT, "content-package", "wardrobe");
const CANVAS = { w: 512, h: 768 };
const ALPHA_THRESHOLD = "6%"; // 取 alpha>6% 為內容，忽略次可見邊緣雜訊
const GENERATED = join(WARDROBE_DIR, "_shared", "asset-content-box.generated.js");
const apply = process.argv.includes("--apply");

function listLayerAssets() {
  const out = [];
  for (const pack of readdirSync(WARDROBE_DIR, { withFileTypes: true })) {
    if (!pack.isDirectory() || pack.name.startsWith("_")) continue;
    const layersDir = join(WARDROBE_DIR, pack.name, "assets", "layers");
    let entries;
    try { entries = readdirSync(layersDir); } catch { continue; }
    for (const file of entries) {
      if (!file.endsWith(".webp")) continue;
      out.push({ packId: pack.name, name: file.replace(/\.webp$/, ""), path: join(layersDir, file) });
    }
  }
  return out;
}

function contentBox(file) {
  const geom = execFileSync(
    "magick",
    [file, "-channel", "A", "-separate", "+channel", "-threshold", ALPHA_THRESHOLD, "-format", "%@", "info:"],
    { encoding: "utf8" }
  ).trim();
  const m = /^(\d+)x(\d+)\+(\d+)\+(\d+)$/.exec(geom);
  if (!m) return null;
  const w = Number(m[1]); const h = Number(m[2]); const x = Number(m[3]); const y = Number(m[4]);
  return { w, h, left: x, top: y, right: x + w, bottom: y + h };
}

const assets = listLayerAssets();
const map = {};
const anomalies = [];
let cropped = 0;

for (const a of assets) {
  const box = contentBox(a.path);
  if (!box || box.w === 0 || box.h === 0) { anomalies.push(`${a.packId}/${a.name}: EMPTY/transparent`); continue; }
  const fullCanvas = box.left === 0 && box.top === 0 && box.right === CANVAS.w && box.bottom === CANVAS.h;
  if (fullCanvas) anomalies.push(`${a.packId}/${a.name}: already full-canvas (no margin to trim)`);
  map[`${a.packId}/${a.name}`] = { left: box.left, top: box.top, right: box.right, bottom: box.bottom };
  if (apply && !fullCanvas) {
    execFileSync("magick", [a.path, "-crop", `${box.w}x${box.h}+${box.left}+${box.top}`, "+repage", a.path]);
    cropped += 1;
  }
}

if (apply) {
  const body = Object.entries(map)
    .map(([k, b]) => `  ${JSON.stringify(k)}: { left: ${b.left}, top: ${b.top}, right: ${b.right}, bottom: ${b.bottom} }`)
    .join(",\n");
  const header = "// AUTO-GENERATED by tool/trim-wardrobe-assets.mjs —— 請勿手改。\n"
    + "// 各 wardrobe layer 素材「裁切前」的原始內容框（canvas 512x768 座標）；\n"
    + "// 供 per-item targetBox identity 對位（targetBox = 原始內容框時與裁切前畫面一致）。\n\n";
  writeFileSync(GENERATED, `${header}export const assetContentBoxByPackName = Object.freeze({\n${body}\n});\n`);
  console.log(`APPLIED: cropped ${cropped}/${assets.length} assets; wrote ${relative(ROOT, GENERATED)}`);
} else {
  console.log(`DRY-RUN: measured ${assets.length} assets (threshold ${ALPHA_THRESHOLD}). No files changed.`);
  const sample = Object.entries(map).slice(0, 8);
  for (const [k, b] of sample) console.log(`  ${k.padEnd(48)} box=${b.left},${b.top},${b.right},${b.bottom} (${b.right - b.left}x${b.bottom - b.top})`);
  console.log(`  ...(${Object.keys(map).length} total)`);
}

if (anomalies.length) {
  console.log(`\nANOMALIES (${anomalies.length}):`);
  for (const a of anomalies) console.log(`  - ${a}`);
}
